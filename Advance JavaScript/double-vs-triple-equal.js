// == vs ===

const first = 1;
const second = true;
if (first == second) {
    console.log("condition is true");
} 
else {
    console.log("condition is false");
}

// triple-equal

const first1 = 5;
const second1 = 5;
if (first1 === second1) {
    console.log("condition is true");
}
else {
    console.log("condition is false");
}